package maps;

import java.util.Hashtable;

public class HashTable01 {
    public static void main(String[] args) {
        Hashtable<Integer,Integer> ht = new Hashtable<>();
        ht.put(1,2);
        ht.put(2,4);
        ht.put(6,8);
        System.out.println(ht);//{6=8, 2=4, 1=2}








    }
}
